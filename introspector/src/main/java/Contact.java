public class Contact {
    String name;

    int age;

    public String getName() {
        return name;
    }

    public Contact setName(final String name) {
        this.name = name;
        return this;
    }

    public int getAge() {
        return age;
    }

    public Contact setAge(final int age) {
        this.age = age;
        return this;
    }
}
